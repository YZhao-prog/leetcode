#include <bits/stdc++.h>
using namespace std;

int n, m;
int f[2001], v[2001], w[2001], l[2001];

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    cin >> n >> m;
    for (int i = 1; i <= n; i++) {
        int x, y, z;
        cin >> x >> y >> z;
        v[i] = x, w[i] = y, l[i] = z;
    }

    for (int i = 1; i <= n; i++) {
        int res = l[i];
        for (int k = 1; k <= res; res -= k, k *= 2) {
            for (int j = m; j >= v[i] * k; j--) {
                f[j] = max(f[j], f[j - v[i] * k] + w[i] * k);
            }
        }
        for (int j = m; j >= v[i] * res; j--) {
            f[j] = max(f[j], f[j - v[i] * res] + w[i] * res);
        }
    }
    cout << f[m];
}
